#include <iostream>
#include <cmath>
using namespace std;

int main() {
    
    int n=5;
    int m=10;

    cout<<min(n,m)<<endl; // find minimum
    cout<<max(n,m)<<endl;  // find maximum
    cout<<sqrt(100)<<endl; // find square root
    cout<<pow(2,3)<<endl; // find power
    cout<<round(5.4)<<endl; // rounding off
    cout<<ceil(5.1)<<endl; // rounding off to highest
    cout<<floor(6.9)<<endl; // rounding off to lowest 
    cout<<log(2)<<endl; // finding log 

    return 0;
}