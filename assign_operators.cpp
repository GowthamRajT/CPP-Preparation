#include <iostream>
using namespace std;

int main() {
    //Your Code goes here!
    int age = 20;
    cout<<"age is "<<age<<endl;
    age += 5; // works like age = age + 5;
    cout<<"age is "<<age<<endl;
    age -= 5; // works like age = age - 5;
    cout<<"age is "<<age<<endl;
    age *= 5; // works like age = age * 5;
    cout<<"age is "<<age<<endl;
    age /= 5; // works like age = age / 5;
    cout<<"age is "<<age<<endl;
    age %= 5; // works like age = age % 5;
    cout<<"age is "<<age<<endl;

    return 0;
}