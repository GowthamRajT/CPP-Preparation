#include <iostream>
using namespace std;

int main() {
    //Your Code goes here!
    int a=10;

    cout<<"a is "<<a++<<endl;
    cout<<"a is "<<a<<endl; // increment

    cout<<"a is "<<a--<<endl;
    cout<<"a is "<<a<<endl; // decrement

    cout<<"a is "<<++a<<endl; // preincrement
    cout<<"a is "<<--a<<endl; //predecrement
    return 0;
}